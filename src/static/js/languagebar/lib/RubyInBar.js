Ruby.prototype.generateOfficialDocsURL = function (version, topic) {
  if (topic === 'Choose a topic'){
    return this.baseUrl + version + "/"
  } else {
    topic = topic.replace("::", "/");
    return this.baseUrl + version + "/" + topic + ".html";
  }
};

